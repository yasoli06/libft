#include <stdio.h>

int	ft_tolower(int *str)
{
	int	a;

	a = 0;
	if (*str == '\0')
	{
		return (1);
	}
	while (str[a] != '\0')
	{
		if (str[a] >= 'a' && str[a] <= 'z')
		{
			a++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
